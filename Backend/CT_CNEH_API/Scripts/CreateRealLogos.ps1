# Script pour créer de vraies images PNG de test
Write-Host "Création d'images PNG valides..." -ForegroundColor Green

# Chemin des logos
$logosPath = "wwwroot\uploads\logos"

# Créer une image PNG simple (1x1 pixel bleu)
$pngBytes = [byte[]]@(
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, 0x00, 0x00, 0x00,
    0x0C, 0x49, 0x44, 0x41, 0x54, 0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00,
    0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC, 0x33,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
)

# Remplacer les fichiers existants
$logoFiles = @("logo_reseau_nord.png", "logo_reseau_centre.png")
foreach ($file in $logoFiles) {
    $filePath = Join-Path $logosPath $file
    [System.IO.File]::WriteAllBytes($filePath, $pngBytes)
    Write-Host "✓ Créé: $file" -ForegroundColor Green
}

Write-Host "Images PNG créées avec succès !" -ForegroundColor Green






